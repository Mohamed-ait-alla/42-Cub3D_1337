#include "../../../includes/cub3d.h"

int	custom_error(char *msg)
{
	int	i;

	ft_malloc(0, 0);
	i = 0;
	while (msg[i])
	{
		write(2, &msg[i], 1);
		i++;
	}
	return (1);
}
